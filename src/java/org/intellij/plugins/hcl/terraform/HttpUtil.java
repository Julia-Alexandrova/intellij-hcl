package org.intellij.plugins.hcl.terraform;

import org.apache.http.client.methods.CloseableHttpResponse;
import org.apache.http.client.methods.HttpRequestBase;
import org.apache.http.impl.client.CloseableHttpClient;

import java.io.IOException;

public class HttpUtil {
  // For some reason Kotlin 1.1 fails to compile that invocation
  public static CloseableHttpResponse execute(CloseableHttpClient client, HttpRequestBase request) throws IOException {
    return client.execute(request);
  }
}
